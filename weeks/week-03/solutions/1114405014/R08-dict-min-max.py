# R8. 字典運算：min/max/sorted + zip（1.8）
# =========================================================
# 字典本身沒有直接的「最大/最小值」概念
# 需要配合 zip() 將鍵值對轉換，或使用 key 參數
# =========================================================

prices = {'ACME': 45.23, 'AAPL': 612.78, 'FB': 10.75}

# =========================================================
# 方法一：使用 zip() 將 dict 轉換為鍵值對元組
# =========================================================

# zip(prices.values(), prices.keys()) 創建 (值, 鍵) 元組的迭代器
# min() 比較第一個元素（價格），返回整個元組
# 返回：(10.75, 'FB') —— 最便宜的股票
min(zip(prices.values(), prices.keys()))

# max() 返回最高價格的股票
# 返回：(612.78, 'AAPL')
max(zip(prices.values(), prices.keys()))

# sorted() 按價格排序（升序）
# 返回：[(10.75, 'FB'), (45.23, 'ACME'), (612.78, 'AAPL')]
sorted(zip(prices.values(), prices.keys()))

# =========================================================
# 方法二：直接對鍵使用 key 參數（更直觀，返回鍵而非鍵值對）
# =========================================================

# key 參數指定用於比較的值來源
# prices[k] 返回鍵 k 對應的價格
# min() 返回的是鍵，而非鍵值對
# 返回：'FB' —— 最便宜股票的代碼
min(prices, key=lambda k: prices[k])

# max() 返回最貴股票代碼
max(prices, key=lambda k: prices[k])

# sorted() 返回按價格排序的鍵列表
sorted(prices, key=lambda k: prices[k])
