# R3. deque 保留最後 N 筆（1.3）
# =========================================================
# deque（雙端佇列）是一種同時支援從兩端高效添加和移除元素的資料結構
# 當設定 maxlen 時，超過長度的元素會自動被移除
# 常用於實現「保留最近 N 筆數據」的功能，如歷史記錄、滑动窗口等
# =========================================================

from collections import deque

# 建立有最大長度限制的 deque
# maxlen=3 表示最多隻保留3個元素
q = deque(maxlen=3)

# 添加元素到右端（尾部）
q.append(1)
q.append(2)
q.append(3)

# 當添加第4個元素時，最左側最舊的元素會自動被移除
# 結果：q = [2, 3, 4]，元素 1 被移除
q.append(4)  # 自動丟掉最舊的 1

# 建立無長度限制的 deque
q = deque()

# 從右端添加元素
q.append(1)

# 從左端（頭部）添加元素
q.appendleft(2)
# 現在 q = [2, 1]

# 從右端移除並返回元素（類似棧的 pop）
q.pop()
# 返回並移除 1，q 變為 [2]

# 從左端移除並返回元素
q.popleft()
# 返回並移除 2，q 變為 []
