# R11. 命名切片 slice（1.11）
# =========================================================
# slice 物件用於表示切片，可以給切片起一個有意義的名字
# 相比直接寫 slice(20, 23)，使用命名切片有以下優點：
# 1. 程式碼更易讀，SHARES 比數字 20:23 更明確意圖
# 2. 易於維護：如果切片範圍改變，只需修改一處
# 3. 可重用：同一個 slice 物件可用於多個切片操作
# =========================================================

# 模擬一個固定的資料格式字串
# 位置 0-19：填充（20個字元）
# 位置 20-22：股票股數（3個字元）
# 位置 23-30：填充（8個字元）
# 位置 31-36：股票價格（6個字元）
# 位置 37-47：填充（11個字元）
record = '....................100 .......513.25 ..........'

# 定義命名切片
# SHARES 代表股數欄位：位置 20 到 22（不包含 23）
SHARES = slice(20, 23)

# PRICE 代表價格欄位：位置 31 到 37（不包含 37）
PRICE = slice(31, 37)

# 使用命名切片進行切片操作
# record[SHARES] 相當於 record[20:23]，得到 '100'
# record[PRICE] 相當於 record[31:37]，得到 '513.25'
cost = int(record[SHARES]) * float(record[PRICE])
# 計算結果：100 * 513.25 = 51325.0

