# R9. 兩字典相同點：keys/items 集合運算（1.9）
# =========================================================
# Python 字典的 keys()、values()、items() 返回的都是「視圖對象」
# dict_keys、dict_values、dict_items 支援集合運算：
# & (交集)、| (聯集)、- (差集)、^ (對稱差集)
# =========================================================

a = {'x': 1, 'y': 2, 'z': 3}
b = {'w': 10, 'x': 11, 'y': 2}

# =========================================================
# keys() 的集合運算（比較鍵）
# =========================================================

# 交集：兩個字典共有的鍵
# 返回：{'x', 'y'}
a.keys() & b.keys()

# 差集：在 a 中但不在 b 中的鍵
# 返回：{'z'}
a.keys() - b.keys()

# 在 b 中但不在 a 中的鍵
# 返回：{'w'}
b.keys() - a.keys()

# 對稱差集：只在其中一個字典出現的鍵
# 返回：{'z', 'w'}
a.keys() ^ b.keys()

# =========================================================
# items() 的集合運算（比較鍵值對）
# =========================================================

# 交集：鍵值對完全相同的項目
# 返回：{('y', 2)} —— 只有 'y': 2 兩個字典都相同
a.items() & b.items()

# 差集：在 a 中但鍵值對不完全相同於 b 中任何項目的鍵
a.items() - b.items()
# 返回：{('x', 1), ('z', 3)} —— 'x':1 vs 'x':11 值不同，'z':3 在 b 中沒有

# =========================================================
# 實用範例：字典推導式 + 集合運算 過濾鍵
# =========================================================

# 建立新字典，只保留在 a 中但不在 {'z', 'w'} 中的鍵值對
# a.keys() - {'z', 'w'} = {'x', 'y'}
# 結果：{'x': 1, 'y': 2}
c = {k: a[k] for k in a.keys() - {'z', 'w'}}

