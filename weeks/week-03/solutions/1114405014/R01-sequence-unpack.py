# R1. 序列解包（1.1）
# =========================================================
# 序列解包（Sequence Unpacking）允許將一個序列（如元組或列表）
# 的元素同時賦值給多個變數，這是 Python 中非常實用的語法特性。
# =========================================================

# 基本解包：將元組 (4, 5) 的元素分別解包到 x 和 y
# 解包後：x = 4, y = 5
p = (4, 5)
x, y = p

# 完整解包：可以一次將列表中的所有元素解包到各自變數
# data 包含四個元素：名稱、股數、價格、日期（元組）
data = ['ACME', 50, 91.1, (2012, 12, 21)]

# 將 data 解包到四個變數
# name = 'ACME', shares = 50, price = 91.1, date = (2012, 12, 21)
name, shares, price, date = data

# 巢狀解包：當某個元素本身也是序列時，可以進一步解包
# 會將 date 元組內的三個值也解包出來
# year = 2012, mon = 12, day = 21
name, shares, price, (year, mon, day) = data

# 丟棄不需要的值（占位符）
# 使用底線 _ 作為「不在意」的變數名稱，表示只取需要的值
# 這裡只取 shares 和 price，忽略 name 和 date
# 底線只是慣例，任何變數名都可以，但 _ 是 Python 社群共識
_, shares, price, _ = data
