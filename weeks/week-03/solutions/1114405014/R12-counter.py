# R12. Counter 統計 + most_common（1.12）
# =========================================================
# Counter 是用於計數的可視為「多集合」（Multi-set）的字典子類
# 主要用途：統計可迭代物件中每個元素出現的次數
# 類似於其他語言中的「Bag」或「Multiset」資料結構
# =========================================================

from collections import Counter

# 單詞列表
words = ['look', 'into', 'my', 'eyes', 'look']

# 建立計數器物件
# Counter 會自動統計每個單詞出現的次數
# word_counts = Counter({'look': 2, 'into': 1, 'my': 1, 'eyes': 1})
word_counts = Counter(words)

# most_common(n): 返回最常見的 n 個元素，按次數降序排列
# 如果 n 省略或為 None，返回所有元素
# 返回：[('look', 2), ('into', 1), ('my', 1), ('eyes', 1)]
word_counts.most_common(3)

# =========================================================
# Counter 的其他常用操作
# =========================================================

# update(iterable): 用另一個可迭代物件更新計數器（增量）
word_counts.update(['eyes', 'eyes'])
# 'eyes' 的計數從 1 增加到 3

# 元素访问：如同字典
# word_counts['look'] 返回 2

